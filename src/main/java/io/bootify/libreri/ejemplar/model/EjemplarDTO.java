package io.bootify.libreri.ejemplar.model;


public class EjemplarDTO {

    private Integer idEjemplar;
    private Integer libro;
    private Integer revista;

    public Integer getIdEjemplar() {
        return idEjemplar;
    }

    public void setIdEjemplar(final Integer idEjemplar) {
        this.idEjemplar = idEjemplar;
    }

    public Integer getLibro() {
        return libro;
    }

    public void setLibro(final Integer libro) {
        this.libro = libro;
    }

    public Integer getRevista() {
        return revista;
    }

    public void setRevista(final Integer revista) {
        this.revista = revista;
    }

}
